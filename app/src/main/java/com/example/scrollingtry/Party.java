package com.example.scrollingtry;

public class Party {
    String name;
    String place;
    int range;
    int age;
    String type;
    String price;
    String time;
    String image;
    String description;

    public Party(String name, String place, int range, int age, String type, String price, String time, String image, String description) {
        this.name = name;
        this.place = place;
        this.range = range;
        this.age = age;
        this.type = type;
        this.price = price;
        this.time = time;
        this.image = image;
        this.description = description;
    }
}
